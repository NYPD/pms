package services.ntr.pms.model.information;

public class Emblem {
	
	private String portal;
	private String wowp;
	private String wot;
	
	public String getPortal()
	{
		return portal;
	}
	public void setPortal(String portal)
	{
		this.portal = portal;
	}
	public String getWowp()
	{
		return wowp;
	}
	public void setWowp(String wowp)
	{
		this.wowp = wowp;
	}
	public String getWot()
	{
		return wot;
	}
	public void setWot(String wot)
	{
		this.wot = wot;
	}
}
